function slugify(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
}

function getLogo(name) {
  return name
    .replace(/[^A-Za-z0-9]/g, '')
    .slice(0, 2)
    .toUpperCase();
}

function inferCategory(toolType, name, description) {
  const source = `${name} ${description}`.toLowerCase();

  if (toolType === 'instagram') {
    if (source.includes('hashtag')) return 'Hashtag Research';
    if (source.includes('analytic') || source.includes('insight')) return 'Analytics';
    if (source.includes('schedule') || source.includes('planner')) return 'Scheduling';
    if (source.includes('engagement')) return 'Engagement';
    if (source.includes('automation') || source.includes('bot')) return 'Automation';
    return 'Growth';
  }

  if (source.includes('video')) return 'Video';
  if (source.includes('image') || source.includes('design') || source.includes('art')) return 'Design';
  if (source.includes('audio') || source.includes('music')) return 'Audio';
  if (source.includes('website') || source.includes('wordpress')) return 'Website';
  if (source.includes('productivity')) return 'Productivity';
  if (source.includes('writing') || source.includes('copy')) return 'Writing';
  return 'AI Tools';
}

function inferPricing(name, index) {
  if (name === 'ChatGPT' || name === 'Buffer' || name === 'Later' || name === 'Canva AI') {
    return 'Freemium';
  }

  return index % 3 === 0 ? 'Freemium' : 'Paid';
}

function inferRating(index) {
  return Number((4.6 + (index % 4) * 0.1).toFixed(1));
}

function inferHighlights(toolType, category) {
  if (toolType === 'instagram') {
    return [
      { label: 'Category', value: category },
      { label: 'Use Case', value: 'Instagram growth' },
      { label: 'Fit', value: 'Creators and brands' },
    ];
  }

  return [
    { label: 'Category', value: category },
    { label: 'Use Case', value: 'AI workflow' },
    { label: 'Fit', value: 'Creators and teams' },
  ];
}

function inferUseCases(toolType, name, description) {
  if (toolType === 'instagram') {
    return [
      `Evaluate ${name} for content planning and audience growth workflows.`,
      `Use it alongside scheduling, analytics, or hashtag research to improve reach.`,
      description,
    ];
  }

  return [
    `Test ${name} in a high-leverage AI workflow before expanding your stack.`,
    `Use it to support content, automation, or creative production tasks.`,
    description,
  ];
}

function inferPros(toolType, description) {
  return [
    'Recognizable brand name with clear buyer intent.',
    description,
    toolType === 'instagram'
      ? 'Fits naturally into creator and social media growth workflows.'
      : 'Supports high-CPC AI software and productivity search intent.',
  ];
}

function inferCons(toolType) {
  return [
    'Feature depth and pricing should still be reviewed on the official site.',
    toolType === 'instagram'
      ? 'Some growth workflows may require careful compliance review.'
      : 'AI output quality depends on the specific use case and workflow design.',
  ];
}

function expandTools(sourceTools, toolType) {
  return sourceTools.map((tool, index) => {
    const category = inferCategory(toolType, tool.name, tool.desc);
    const slug = slugify(tool.name);

    return {
      slug,
      name: tool.name,
      logo: getLogo(tool.name),
      toolType,
      category,
      pricing: inferPricing(tool.name, index),
      rating: inferRating(index),
      featured: index < 4,
      affiliateUrl: tool.url,
      description: tool.desc,
      fullDescription: `${tool.name} is listed in OGTools as a ${category.toLowerCase()} option for ${toolType === 'instagram' ? 'Instagram growth' : 'AI workflows'}. ${tool.desc}`,
      highlights: inferHighlights(toolType, category),
      useCases: inferUseCases(toolType, tool.name, tool.desc),
      pros: inferPros(toolType, tool.desc),
      cons: inferCons(toolType),
    };
  });
}

